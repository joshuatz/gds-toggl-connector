/**
 * @override
 * This is used by Data Studio to build the configuration UI screen that the user enters settings on first run
 */
function getConfig() {
	// Get config obj provided by gds-connector
	let config = cc.getConfig();

	// Set config general info
	config.newInfo()
		.setId('instructions')
        .setText('Configure the connector for Toggl');
    
    // Require workspace ID (necessary for all report API calls)
    config.newTextInput()
        .setId('workspaceId')
        .setName('Toggl Workspace ID')
        .setHelpText('Numerical ID for your workspace')
        .setPlaceholder('123456');

    // Allow pre-filtering to just billable time
    config.newCheckbox()
        .setId('prefilterBillable')
        .setName('Pre-filter Billable')
        .setHelpText('Pre-filter all reports to just "billable" time (as configured in Toggl)');

    // Require date range to be sent with every data request
    config.setDateRangeRequired(true);

	// Make sure to return the built config
	return config.build();
}

/**
 * @override
 * Kept as separate function from getSchema, so it can be reused with getData()
 * https://developers.google.com/datastudio/connector/reference#getschema
 * List of types: https://developers.google.com/datastudio/connector/reference#datatype
 */
function getFields(){
    let fields = cc.getFields();
    let types = cc.FieldType;
    let aggregations = cc.AggregationType;

    // Default date/time dimension
    fields
        .newDimension()
        .setId('day')
        .setName('Date')
        .setType(types.YEAR_MONTH_DAY);
    fields
        .newDimension()
        .setId('startedAtHour')
        .setName('Started At - Hour')
        .setType(types.YEAR_MONTH_DAY_HOUR);

    // Projects
    fields
        .newDimension()
        .setId('projectId')
        .setName('Project Id')
        .setDescription('Numerical ID generated by Toggl for each project')
        .setType(types.NUMBER);
    fields
        .newDimension()
        .setId('projectName')
        .setName('Project Name')
        .setDescription('Your project name')
        .setType(types.TEXT);
    // Clients
    fields
        .newDimension()
        .setId('clientId')
        .setName('Client Id')
        .setDescription('Numerical ID generated by Toggl for each client')
        .setType(types.NUMBER);
    fields
        .newDimension()
        .setId('clientName')
        .setName('Client Name')
        .setDescription('Client Name')
        .setType(types.TEXT);

    // Billing Info
    fields
        .newMetric()
        .setId('billableMoneyTotal')
        .setName('Total Billable Amount')
        .setDescription('Total Billable Amount in Configured Currency')
        .setType(types.CURRENCY_USD);
    fields
        .newMetric()
        .setId('billableTimeTotal')
        .setName('Total Billable Hours')
        .setDescription('Total Billable Time, as configured in Toggl')
        .setType(types.DURATION);
    fields
        .newMetric()
        .setId('isBillable')
        .setName('Is Billable')
        .setDescription('Whether given entry was marked as billable')
        .setType(types.BOOLEAN);

    // Time
    fields
        .newMetric()
        .setId('time')
        .setName('Time')
        .setDescription('Logged Time')
        .setType(types.DURATION)

    return fields;
}

/**
 * @override
 */
function getSchema(request:SchemaRequest) {
    return {schema : getFields().build()};
}

